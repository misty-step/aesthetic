/* build the registry: static JSON served from the site (site/r/) in
   the shadcn universal registry-item shape, so
   `npx shadcn@latest add https://aesthetic.mistystep.io/r/aesthetic.json`
   installs the system into any project — no npm account on either
   side. Regenerated by the release script; never edit by hand. */
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const read = (p) => readFileSync(join(root, p), 'utf8');
const version = JSON.parse(read('package.json')).version;

const items = [
  {
    name: 'aesthetic',
    type: 'registry:item',
    title: 'aesthetic',
    description: `The Misty Step design system, v${version}: one stylesheet — tokens, base, and the primitive catalog. Hierarchy from ink and weight, hairlines, radius 0, motion as feedback.`,
    files: [
      {
        path: 'aesthetic.css',
        type: 'registry:file',
        target: '~/aesthetic.css',
        content: read('aesthetic.css'),
      },
      {
        path: 'tokens.json',
        type: 'registry:file',
        target: '~/aesthetic.tokens.json',
        content: read('tokens.json'),
      },
    ],
  },
  {
    name: 'recipes',
    type: 'registry:item',
    title: 'aesthetic recipes',
    description: `The canonical behavior glue for aesthetic v${version}: mode toggle, nav indicator, view swap, send moment, settings rows, anticipation, toasts, popovers. Dependency-free.`,
    files: [
      {
        path: 'recipes/recipes.js',
        type: 'registry:file',
        target: '~/aesthetic.recipes.js',
        content: read('recipes/recipes.js'),
      },
    ],
  },
];

const outDir = join(root, 'site', 'r');
mkdirSync(outDir, { recursive: true });

writeFileSync(
  join(outDir, 'registry.json'),
  JSON.stringify(
    {
      $schema: 'https://ui.shadcn.com/schema/registry.json',
      name: 'misty-step',
      homepage: 'https://aesthetic.mistystep.io',
      items: items.map(({ name, type, title, description, files }) => ({
        name,
        type,
        title,
        description,
        files: files.map(({ path, type: t, target }) => ({
          path,
          type: t,
          target,
        })),
      })),
    },
    null,
    2,
  ) + '\n',
);

for (const item of items) {
  writeFileSync(
    join(outDir, `${item.name}.json`),
    JSON.stringify(
      { $schema: 'https://ui.shadcn.com/schema/registry-item.json', ...item },
      null,
      2,
    ) + '\n',
  );
}

console.log(
  `site/r/: registry.json + ${items.map((i) => i.name + '.json').join(' + ')} (v${version})`,
);
